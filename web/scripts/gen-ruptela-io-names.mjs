// Generates src/services/ruptela-io-names.ts from the Ruptela OID dictionary
// (model-garage/pkg/ruptela/schema/oids.csv). That CSV is the device vendor's
// source of truth: column 0 = IO ID, column 1 = Name, column 7 = Units.
//
// Usage:
//   node scripts/gen-ruptela-io-names.mjs [path/to/oids.csv]
//   RUPTELA_OIDS_CSV=/abs/path/oids.csv node scripts/gen-ruptela-io-names.mjs
//
// With no argument it looks for the model-garage checkout as a sibling of this
// repo. Re-run whenever oids.csv changes and commit the regenerated output.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');        // b2b-fleet-mgr-app/web
const sourceRoot = resolve(repoRoot, '..', '..'); // .../Source

const csvPath =
    process.argv[2] ||
    process.env.RUPTELA_OIDS_CSV ||
    resolve(sourceRoot, 'model-garage', 'pkg', 'ruptela', 'schema', 'oids.csv');

const outPath = resolve(repoRoot, 'src', 'services', 'ruptela-io-names.ts');

// RFC-4180 parser over the whole file: quoted fields may contain commas AND
// embedded newlines (the dictionary's Notes column wraps across lines), so we
// tokenize the entire text rather than splitting on physical lines first.
function parseCsv(text) {
    const records = [];
    let row = [];
    let cur = '';
    let inQuotes = false;
    for (let i = 0; i < text.length; i++) {
        const c = text[i];
        if (inQuotes) {
            if (c === '"') {
                if (text[i + 1] === '"') { cur += '"'; i++; }
                else inQuotes = false;
            } else cur += c;
        } else if (c === '"') {
            inQuotes = true;
        } else if (c === ',') {
            row.push(cur);
            cur = '';
        } else if (c === '\n' || c === '\r') {
            if (c === '\r' && text[i + 1] === '\n') i++;
            row.push(cur);
            cur = '';
            records.push(row);
            row = [];
        } else cur += c;
    }
    if (cur !== '' || row.length) { row.push(cur); records.push(row); }
    return records;
}

const NAME_COL = 1;
const UNIT_COL = 7;

const csv = readFileSync(csvPath, 'utf8');
const records = parseCsv(csv);

const entries = new Map(); // id -> { name, unit }
for (let i = 1; i < records.length; i++) {
    const f = records[i];
    const id = parseInt((f[0] ?? '').trim(), 10);
    if (!Number.isInteger(id)) continue;
    const name = (f[NAME_COL] ?? '').trim();
    if (!name) continue;
    let unit = (f[UNIT_COL] ?? '').trim();
    if (unit === '-' || unit === '') unit = '';
    entries.set(id, { name, unit });
}

const ids = [...entries.keys()].sort((a, b) => a - b);

const rows = ids.map(id => {
    const { name, unit } = entries.get(id);
    const n = JSON.stringify(name);
    return unit ? `    ${id}: { name: ${n}, unit: ${JSON.stringify(unit)} },`
                : `    ${id}: { name: ${n} },`;
});

const out = `// AUTO-GENERATED by scripts/gen-ruptela-io-names.mjs from the Ruptela OID
// dictionary (model-garage/pkg/ruptela/schema/oids.csv). Do not edit by hand;
// re-run \`npm run gen:io-names\` and commit the result when the CSV changes.
//
// Maps a Ruptela IO id to its vendor name and (where defined) unit, so the
// telemetry snapshot can show a human-readable label instead of a bare id.

export interface IoMeta {
    name: string;
    unit?: string;
}

export const RUPTELA_IO_META: Record<number, IoMeta> = {
${rows.join('\n')}
};

// getIoName returns the vendor name for an IO id, suffixed with its unit when
// one is defined (e.g. "OBD vehicle speed (km/h)"). Unknown ids fall back to
// "IO <id>" so a row still reads as something.
export function getIoName(id: number): string {
    const meta = RUPTELA_IO_META[id];
    if (!meta) return \`IO \${id}\`;
    return meta.unit ? \`\${meta.name} (\${meta.unit})\` : meta.name;
}
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
// eslint-disable-next-line no-console
console.log(`Wrote ${ids.length} IO names to ${outPath} (source: ${csvPath})`);
